
def toStr(input):
    try:
        return str(input)
    except:
        return input

def tryToStr(input):
    try:
        return str(input)
    except:
        return None

def toInt(input):
    try:
        return int(input)
    except:
        return input

def tryToInt(input):
    try:
        return int(input)
    except:
        return None